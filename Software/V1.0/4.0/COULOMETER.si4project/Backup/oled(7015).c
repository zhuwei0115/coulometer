/* Show Regular Pattern (Full Screen) */
static void fill_RAM(u8 data)
{
	u8 i, j;

	for(i = 0; i < PAGE_TOTAL; i++)						// page total
	{
		set_start_page(i);
		set_start_column(0x00);
		
		IIC_Start();
		IIC_Send_Byte(OLED_ADDR_W);
		IIC_Wait_Ack();
		IIC_Send_Byte(CLEAR_START_LINE);		// Start line
		IIC_Wait_Ack();

		for (j = 0; j < COLUMN_TOTAL; j++)
		{
			IIC_Send_Byte(data);
			IIC_Wait_Ack();
		}

		IIC_Stop();
	}
}

void oled_clear_diplay(void)
{
    fill_RAM(CLEAR_SCREEN);					// Clear Screen
}


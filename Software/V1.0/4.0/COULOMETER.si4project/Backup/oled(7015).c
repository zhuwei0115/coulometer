/**
 * 总共4页，column=64: 8*8
 * 128*64 
 */
static void oled_print_string(u8 *data, u8 start_page, u8 start_column, u8 len)  //输出大号的字母
{
	u8 *src;

	src = data;
	while(len--)
	{
		if (0x20 == *src)
		{
			start_page += 8;
			src++;
			continue;
		}

		oled_print_font(3, *src, 8, start_page, start_column);
		src++;
		start_column += 8;
		if (0x30 == *src)
		{
			break;
		}
	}
}


static void oled_init(void)
{	
	/*
	oled_res_low();
	delay_us(100);		// ? TODO
	oled_res_high();
	*/
	delay_ms(300);
	set_command_lock(UNLOCK_CTL);			// Unlock Driver IC (0x12/0x16)
	set_display_on_off(DISPLAY_OFF);		// Display Off (0xAE/0xAF)	
	set_display_clock(CLOCK_OSC);			// Set Clock as 110 Frames/Sec
	set_multiplex_ratio(MULTI_RATIO);		// 1/32 Duty (0x0F~0x3F)
	set_display_offset(DISPLAY_OFFSET);		// Shift Mapping RAM Counter (0x00~0x3F)
	set_start_line(START_LINE);				// Set Mapping RAM Display Start Line (0x00~0x3F)
	set_low_power(LOW_POWER);				// Set Low Power Display Mode (0x04/0x05)
	set_addressing_mode(MEMORY_ADDR_MODE);	// Set Page Addressing Mode (0x00/0x01/0x02)
	set_segment_remap(SEGMENT_REMAP);		// Set SEG/Column Mapping (0xA0/0xA1)
	set_common_remap(COMMON_REMAP);			// Set COM/Row Scan Direction (0xC0/0xC8)
	set_common_config(COM_PINS_HARDWARE);	// Set Alternative Configuration (0x02/0x12)
	set_contrast_control(BRIGHTNESS);		// Set SEG Output Current
	set_precharge_period(PRECHAGE_PERIOD);	// Set Pre-Charge as 1 Clock & Discharge as 5 
	set_VCOMH(SET_VCOMH);					// Set VCOM Deselect Level
	set_entire_display(ENTIRE_DISPLAY);		// Disable Entire Display On (0xA4/0xA5)
	set_inverse_display(INVERSE_DISPLAY);	// Disable Inverse Display On (0xA6/0xA7)   
	fill_RAM(CLEAR_SCREEN);					// Clear Screen	
	set_display_on_off(DISPLAY_ON);			// Display On (0xAE/0xAF)
	delay_ms(10);
} 

